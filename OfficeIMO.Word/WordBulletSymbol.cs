namespace OfficeIMO.Word {
    /// <summary>
    /// Predefined bullet symbols for custom lists.
    /// </summary>
    public enum WordBulletSymbol : ushort {
        /// <summary>Standard bullet (•).</summary>
        Bullet = '\u2022',
        /// <summary>Black square (■).</summary>
        Square = '\u25A0',
        /// <summary>Black circle (●).</summary>
        BlackCircle = '\u25CF',
        /// <summary>Black diamond (◆).</summary>
        Diamond = '\u25C6',
        /// <summary>Black right arrow (►).</summary>
        Arrow = '\u25BA'
    }
}
